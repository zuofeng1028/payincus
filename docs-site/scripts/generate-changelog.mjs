function git(args) {
  return execFileSync('git', args, {
    cwd: repoRoot,
    encoding: 'utf8',
    stdio: ['ignore', 'pipe', 'pipe']
  }).trim()
}

function safeGit(args, fallback = '') {
  try {
    return git(args)
  } catch {
    return fallback
  }
}

function listTags() {
  const output = safeGit(['tag', '--list', 'v*', '--sort=-v:refname'])
  return output ? output.split('\n').filter(Boolean) : []
}

function commitMeta(ref) {
  const line = safeGit(['log', '-1', '--format=%h%x09%cs%x09%s', ref])
  const [shortHash = '-', date = '-', subject = '-'] = line.split('\t')
  return { shortHash, date, subject: normalizeProductWording(subject) }
}

function commitsBetween(fromRef, toRef) {
  const range = fromRef ? `${fromRef}..${toRef}` : toRef
  const output = safeGit(['log', '--format=%h%x09%s', range])
  if (!output) return []
  return output
    .split('\n')
    .map((line) => {
      const [hash, ...subjectParts] = line.split('\t')
      return { hash, subject: normalizeProductWording(subjectParts.join('\t')) }
    })
    .filter((item) => item.hash && item.subject)
}

function tagContent(tag, format) {
  return safeGit(['tag', '--list', tag, `--format=${format}`]).trim()
}

function normalizeTagNote(value) {
  return value.replace(/\\n/g, '\n')
}

function normalizeProductWording(value) {
  return value
    .replace(/插件中心/g, '扩展中心')
    .replace(/\bPlugin Center\b/g, 'Extension Center')
    .replace(/\bPlugin center\b/g, 'Extension center')
    .replace(/\bplugin center\b/g, 'extension center')
}

function tagBodyLines(tag) {
  const fullContent = normalizeTagNote(tagContent(tag, '%(contents)'))
  const body = normalizeTagNote(tagContent(tag, '%(contents:body)'))
  const firstLine = fullContent.split(/\r?\n/).find((line) => line.trim())
  const startsWithGroupHeading =
    firstLine && /\/ (Fixes and stability|New capabilities|Other changes|Improvements and adjustments|修复与稳定性|新增能力|其他变更|改进与调整)/i.test(firstLine.trim())
  const note = startsWithGroupHeading ? fullContent : (body || fullContent.split(/\r?\n/).slice(1).join('\n'))
  if (!note) return []

  const lines = []
  for (const rawLine of note.split(/\r?\n/)) {
    const line = normalizeProductWording(rawLine.trim())
    if (!line) {
      if (lines.length > 0 && lines[lines.length - 1] !== '') lines.push('')
      continue
    }
    if (!line.startsWith('- ') && / \/ /.test(line)) {
      lines.push(`### ${line.replace(/:$/, '')}`)
      lines.push('')
    } else {
      lines.push(line)
    }
  }

  while (lines[lines.length - 1] === '') lines.pop()
  return lines.length > 0 ? [...lines, ''] : []
}

function classify(subject, locale) {
  const text = subject.toLowerCase()
  if (/(fix|repair|rollback|tolerate|stabilize|harden)/.test(text)) return locale.groups.fixes
  if (/(add|support|allow|enable|create)/.test(text)) return locale.groups.features
  if (/(update|rewrite|prefer|make|prepare|improve|refactor)/.test(text)) return locale.groups.improvements
  return locale.groups.other
}

function groupedCommitLines(commits, locale) {
  if (commits.length === 0) return [locale.duplicateTag, '']

  const groups = new Map()
  for (const commit of commits) {
    const key = classify(commit.subject, locale)
    const items = groups.get(key) ?? []
    items.push(commit)
    groups.set(key, items)
  }

  const lines = []
  for (const [group, items] of groups) {
    lines.push(`### ${group}`)
    lines.push('')
    for (const item of items) {
      lines.push(`- ${item.subject} \`${item.hash}\``)
    }
    lines.push('')
  }
  return lines
}

function render() {
  const tags = listTags()
  const head = commitMeta('HEAD')
  const latestTag = tags[0]
  for (const locale of locales) {
  const lines = [
    `# ${locale.title}`,
    '',
    locale.generatedComment,
    '',
    locale.intro,
    '',
    `## ${locale.currentState}`,
    '',
    `- ${locale.currentHead}: \`${head.shortHash}\``,
    `- ${locale.commitDate}: ${head.date}`,
    `- ${locale.commitSubject}: ${head.subject}`,
    `- ${locale.latestTag}: ${latestTag ? `\`${latestTag}\`` : locale.noTag}`,
    '',
    `## ${locale.unreleased}`,
    ''
  ]

  const unreleased = latestTag ? commitsBetween(latestTag, 'HEAD') : []
  lines.push(...groupedCommitLines(unreleased, locale))

  lines.push(`## ${locale.history}`, '')

  if (tags.length === 0) {
    lines.push(locale.noTags)
  }

  for (let index = 0; index < tags.length; index += 1) {
    const tag = tags[index]
    const nextTag = tags[index + 1]
    const meta = commitMeta(tag)
    const commits = commitsBetween(nextTag, tag)

    lines.push(`## ${tag}`, '')
    lines.push(`- ${locale.releaseCommit}: \`${meta.shortHash}\``)
    lines.push(`- ${locale.commitDate}: ${meta.date}`)
    lines.push(`- ${locale.commitSubject}: ${meta.subject}`)
    lines.push('')
    const releaseNotes = tagBodyLines(tag)
    lines.push(...(releaseNotes.length > 0 ? releaseNotes : groupedCommitLines(commits, locale)))
  }

  lines.push(`## ${locale.generation}`, '')
  lines.push(locale.runBuild, '')
  lines.push('```bash')
  lines.push('pnpm docs:build')
  lines.push('```')
  lines.push('')
  lines.push(locale.runChangelog, '')
  lines.push('```bash')
  lines.push('pnpm docs:changelog')
  lines.push('```')
  lines.push('')
  lines.push(locale.shallowClone)

  mkdirSync(dirname(locale.outputFile), { recursive: true })
  writeFileSync(locale.outputFile, `${lines.join('\n')}\n`, 'utf8')
  console.log(`generated ${locale.outputFile}`)
  }
}

render()
